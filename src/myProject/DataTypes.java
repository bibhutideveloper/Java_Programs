package myProject;

public class DataTypes {
	public static void main(String[] args) {
		System.out.println("Byte Data Type");
		byte b1 = 1;
		byte b2 = 2;
		System.out.println("Sum = "+(b1+b2));
		
		System.out.println("\nShort Data Type");
		byte s1 = 10;
		byte s2 = 20;
		System.out.println("Sum = "+(s1+s2));
		
		System.out.println("\nInt Data Type");
		int i1 = 100;
		int i2 = 200;
		System.out.println("Sum = "+(i1+i2));
		
		System.out.println("\nLong Data Type");
		long l1 = 1000;
		long l2 = 2000;
		System.out.println("Sum = "+(l1+l2));
		
		System.out.println("\nFloat Data Type");
		float f1 = 10.10F;
		float f2 = 20.43F;
		System.out.println("Sum = "+(f1+f2));
		
		System.out.println("\nDouble Data Type");
		double d1 = 1000.32;
		double d2 = 2000.32;
		System.out.println("Sum = "+(d1+d2));
		
		System.out.println("\nString Data Type");
		String str1 = "Hello";
		System.out.println("String = "+str1);
		
		System.out.println("\nChar Data Type");
		char ch = 'a';
		System.out.println("Char = "+ch);
		
		System.out.println("\nBoolean Data Type");
		boolean b = true;
		System.out.println("Boolean = "+b);
	}
}
